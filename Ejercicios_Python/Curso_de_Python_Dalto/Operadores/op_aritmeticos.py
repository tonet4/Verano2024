#suma y resta
suma = 12 + 5
resta = 12 - 5

#multi y div (* y /)
multi = 12 * 5
divi = 12 / 5 #devuelve un dato float

#potenciacion (exponente) (**)
exponente = 12**5

#division baja (//)
division_baja = 12//5 #devuelve entero(int) redondeado hacia bajo

#resto o modulo
resto = 12 % 5

print(f"suma : {suma}")
print(f"resta : {resta}")
print(f"multi : {multi}")
print(f"division : {divi}")
print(f"exponente : {exponente}")
print(f"division baja : {division_baja}")
print(f"resto : {resto}")